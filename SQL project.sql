select top 10
    c.customer_id,
    c.Gender,
    ROUND(SUM(od.total_amount), 2) AS total_spent,
    COUNT(DISTINCT o.order_id) AS orders_count
FROM Customer2 c
JOIN order2 o ON c.customer_id = o.customer_id
JOIN orderdetail2 od ON o.order_id = od.order_id
